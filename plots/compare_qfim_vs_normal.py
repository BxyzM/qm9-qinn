def parse_log(raw: str) -> pd.DataFrame:
    rows = []
    for line in raw.strip().splitlines():
        line = line.strip()
        if not line or line.startswith("epoch"):
            continue
        parts = [p.strip() for p in line.split(",")]
        if len(parts) != 8:
            continue
        try:
            rows.append(
                {
                    "epoch": int(float(parts[0])),
                    "train_loss": float(parts[1]),
                    "train_mae": float(parts[2]),
                    "val_loss": float(parts[3]),
                    "val_mae": float(parts[4]),
                    "lr": float(parts[5]),
                    "seconds": float(parts[6]),
                    "best_val": float(parts[7]),
                }
            )
        except ValueError:
            continue

    frame = pd.DataFrame(rows).sort_values("epoch").reset_index(drop=True)
    return frame


def summarize(name: str, df: pd.DataFrame) -> dict:
    best_idx = df["val_loss"].idxmin()
    return {
        "name": name,
        "best_val_loss": float(df.loc[best_idx, "val_loss"]),
        "best_epoch": int(df.loc[best_idx, "epoch"]),
        "final_val_loss": float(df.iloc[-1]["val_loss"]),
        "final_val_mae": float(df.iloc[-1]["val_mae"]),
        "mean_seconds": float(df["seconds"].mean()),
        "total_seconds": float(df["seconds"].sum()),
    }


def main() -> None:
    out_dir = Path(__file__).resolve().parent
    qfim = parse_log(QFIM_RAW)
    normal = parse_log(NORMAL_RAW)

    qfim_stats = summarize("QFIM", qfim)
    normal_stats = summarize("Normal", normal)

    fig, axes = plt.subplots(1, 3, figsize=(16, 4.5))

    axes[0].plot(qfim["epoch"], qfim["val_loss"], label="QFIM", linewidth=2)
    axes[0].plot(normal["epoch"], normal["val_loss"], label="Normal", linewidth=2)
    axes[0].set_title("Validation Loss")
    axes[0].set_xlabel("Epoch")
    axes[0].set_ylabel("val_loss")
    axes[0].grid(alpha=0.3)
    axes[0].legend()

    axes[1].plot(qfim["epoch"], qfim["val_mae"], label="QFIM", linewidth=2)
    axes[1].plot(normal["epoch"], normal["val_mae"], label="Normal", linewidth=2)
    axes[1].set_title("Validation MAE")
    axes[1].set_xlabel("Epoch")
    axes[1].set_ylabel("val_mae")
    axes[1].grid(alpha=0.3)

    axes[2].plot(qfim["epoch"], qfim["seconds"], label="QFIM", linewidth=2)
    axes[2].plot(normal["epoch"], normal["seconds"], label="Normal", linewidth=2)
    axes[2].set_title("Seconds per Epoch")
    axes[2].set_xlabel("Epoch")
    axes[2].set_ylabel("seconds")
    axes[2].grid(alpha=0.3)

    fig.suptitle("QFIM vs Normal (QM9) - Verlauf und Laufzeit", fontsize=13)
    fig.tight_layout()

    out_path = out_dir / "qfim_vs_normal_comparison.png"
    fig.savefig(out_path, dpi=160)

    improvement_abs = qfim_stats["best_val_loss"] - normal_stats["best_val_loss"]
    improvement_pct = (improvement_abs / qfim_stats["best_val_loss"]) * 100.0
    speedup = qfim_stats["mean_seconds"] / normal_stats["mean_seconds"]

    print(f"Saved plot: {out_path}")
    print("--- Summary ---")
    print(
        f"QFIM   best val_loss={qfim_stats['best_val_loss']:.8f} @ epoch {qfim_stats['best_epoch']}, "
        f"final val_loss={qfim_stats['final_val_loss']:.8f}, avg sec/epoch={qfim_stats['mean_seconds']:.2f}"
    )
    print(
        f"Normal best val_loss={normal_stats['best_val_loss']:.8f} @ epoch {normal_stats['best_epoch']}, "
        f"final val_loss={normal_stats['final_val_loss']:.8f}, avg sec/epoch={normal_stats['mean_seconds']:.2f}"
    )
    print(
        f"Best-val improvement (Normal over QFIM): {improvement_abs:.8f} "
        f"({improvement_pct:.3f}% lower val_loss)"
    )
    print(f"Runtime speedup (QFIM / Normal): {speedup:.2f}x")


if __name__ == "__main__":
    main()
